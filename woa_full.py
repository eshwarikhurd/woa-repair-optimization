def fmerge(tree):
    """Eq. 5 — avg depth of XOR merger nodes. Lower = better."""
    node_count = {}
    for path in tree:
        for n in path[:-1]:
            node_count[n] = node_count.get(n, 0) + 1
    mergers = {n for n, c in node_count.items() if c > 1}
    if not mergers:
        return sum(len(p)-1 for p in tree) / len(tree)
    depths = []
    for m in mergers:
        d = max((p.index(m) for p in tree if m in p), default=0)
        depths.append(d)
    return sum(depths) / len(depths)

def repair_traffic(tree, block_size=6):
    """Total data volume moved (MB). Lower = better."""
    total_hops = sum(len(path) - 1 for path in tree)
    return total_hops * block_size

def fnet_load(tree, block_size=6):
    """Eq. 6 — std dev of link utilization. Lower = better."""
    traffic = {e: 0 for e in EDGE_SET}
    for path in tree:
        for i in range(len(path)-1):
            e = (min(path[i], path[i+1]), max(path[i], path[i+1]))
            if e in traffic: traffic[e] += 1
    utils = [(t * block_size) / bw
             for (e, bw), t in zip(EDGE_SET.items(), traffic.values())]
    mean_u = sum(utils) / len(utils)
    return math.sqrt(sum((u - mean_u)**2 for u in utils) / len(utils))

def fitness(tree):
    return 0.5 * fmerge(tree) + 0.5 * fnet_load(tree)

def all_metrics(tree, block_size=6):
    return {
        'latency':  fmerge(tree),
        'traffic':  repair_traffic(tree, block_size),
        'load_std': fnet_load(tree, block_size)
    }

