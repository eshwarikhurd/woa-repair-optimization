# ─────────────────────────────────────────────
# 2. DIJKSTRA + LOOP REMOVAL
# ─────────────────────────────────────────────

def dijkstra(src, dst):
    dist = {n: -math.inf for n in GRAPH}
    dist[src] = math.inf
    prev = {n: None for n in GRAPH}
    heap = [(-math.inf, src)]
    visited = set()
    while heap:
        neg_d, u = heapq.heappop(heap)
        if u in visited: continue
        visited.add(u)
        for v, bw in GRAPH[u].items():
            new_bw = min(-neg_d, bw)
            if new_bw > dist[v]:
                dist[v] = new_bw
                prev[v] = u
                heapq.heappush(heap, (-new_bw, v))
    path, cur = [], dst
    while cur is not None:
        path.append(cur); cur = prev[cur]
    path.reverse()
    return path if (path and path[0] == src) else [src, dst]

def remove_loops(path):
    seen, clean = set(), []
    for n in path:
        if n not in seen:
            seen.add(n); clean.append(n)
    return clean

def build_path(src, dst):
    return remove_loops(dijkstra(src, dst))

