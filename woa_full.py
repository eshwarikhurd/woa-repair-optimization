def woa(leaves, root, k, n_whales=100, max_iter=100):
    pop = [random_tree(leaves, root, k) for _ in range(n_whales)]
    best = min(pop, key=fitness)
    history = []

    for t in range(max_iter):
        a = 2 - 2 * (t / max_iter)
        for i in range(len(pop)):
            h = random.uniform(0, 40)
            p = random.random()
            candidate = copy.deepcopy(pop[i])
            idx = random.randint(0, k-1)

            if h < 20:
                candidate[idx] = build_path(random.choice(leaves), root)
            elif p < 0.5:
                candidate = copy.deepcopy(best)
                candidate[idx] = build_path(random.choice(leaves), root)
            else:
                path = candidate[idx]
                if len(path) > 2:
                    mid = path[len(path)//2]
                    new_path = build_path(path[0], mid) + build_path(mid, root)[1:]
                    candidate[idx] = remove_loops(new_path)

            if all(len(p) > 0 for p in candidate) and \
               fitness(candidate) < fitness(pop[i]):
                pop[i] = candidate
            if fitness(pop[i]) < fitness(best):
                best = copy.deepcopy(pop[i])

        history.append(fitness(best))

    return best, history

