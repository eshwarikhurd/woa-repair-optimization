# 7. GA
# ─────────────────────────────────────────────

def tournament_select(pop, k_tour=3):
    contestants = random.sample(pop, k_tour)
    return min(contestants, key=fitness)

def crossover(parent1, parent2):
    # swap a random sub-path between parents
    child = copy.deepcopy(parent1)
    idx = random.randint(0, len(parent1)-1)
    child[idx] = copy.deepcopy(parent2[idx])
    return child

def mutate(tree, leaves, root, mutation_rate=0.2):
    tree = copy.deepcopy(tree)
    for idx in range(len(tree)):
        if random.random() < mutation_rate:
            tree[idx] = build_path(random.choice(leaves), root)
    return tree

def ga(leaves, root, k, pop_size=100, max_iter=100):
    pop     = [random_tree(leaves, root, k) for _ in range(pop_size)]
    best    = min(pop, key=fitness)
    history = []

    for t in range(max_iter):
        new_pop = []
        # elitism: keep top 10%
        pop.sort(key=fitness)
        new_pop.extend(copy.deepcopy(pop[:pop_size//10]))

        while len(new_pop) < pop_size:
            p1 = tournament_select(pop)
            p2 = tournament_select(pop)
            child = crossover(p1, p2)
            child = mutate(child, leaves, root)
            if all(len(p) > 0 for p in child):
                new_pop.append(child)

        pop = new_pop
        gen_best = min(pop, key=fitness)
        if fitness(gen_best) < fitness(best):
            best = copy.deepcopy(gen_best)
        history.append(fitness(best))

    return best, history

