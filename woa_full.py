# 6. PSO
# ─────────────────────────────────────────────

def pso(leaves, root, k, n_particles=100, max_iter=100):
    particles   = [random_tree(leaves, root, k) for _ in range(n_particles)]
    pbest       = [copy.deepcopy(p) for p in particles]
    gbest       = min(particles, key=fitness)
    history     = []

    for t in range(max_iter):
        w  = 0.9 - 0.5 * (t / max_iter)   # inertia linearly decreases
        c1, c2 = 1.5, 1.5                  # cognitive / social weights

        for i in range(len(particles)):
            candidate = copy.deepcopy(particles[i])
            for idx in range(k):
                r1, r2 = random.random(), random.random()
                # cognitive: pull toward personal best
                if r1 < c1 * 0.5:
                    candidate[idx] = copy.deepcopy(pbest[i][idx])
                # social: pull toward global best
                if r2 < c2 * 0.5:
                    candidate[idx] = copy.deepcopy(gbest[idx])
                # inertia: random re-route with decaying probability
                if random.random() < w:
                    candidate[idx] = build_path(
                        random.choice(leaves), root)

            if all(len(p) > 0 for p in candidate) and \
               fitness(candidate) < fitness(particles[i]):
                particles[i] = candidate
                if fitness(candidate) < fitness(pbest[i]):
                    pbest[i] = copy.deepcopy(candidate)
            if fitness(particles[i]) < fitness(gbest):
                gbest = copy.deepcopy(particles[i])

        history.append(fitness(gbest))

    return gbest, history

